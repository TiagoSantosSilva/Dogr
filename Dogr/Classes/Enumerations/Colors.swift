import UIKit

enum Colors {
    static let background: UIColor = .systemGray6
    static let cell: UIColor = .systemGray5
    static let tint: UIColor = .systemBlue
    static let label: UIColor = .label
    static let secondaryLabel: UIColor = .secondaryLabel

    enum TabBar {
        static let barTint: UIColor = cell
        static let background: UIColor = cell
        static let unselectedItem: UIColor = .secondaryLabel
    }
}
